package 입출력과사칙연산;

import java.util.Scanner;

public class A나누기B {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        double A = sc.nextDouble();
        double B = sc.nextDouble();
        double result = A / B;
        System.out.print(result);
    }
}
